package Entity;

/**
 *
 * @author luuka
 */
public class Account {

    private String user;
    private String pass;
    private String fullName;
    private int role;

    public Account() {
    }

    public Account(String user, String pass, String fullName, int role) {
        this.user = user;
        this.pass = pass;
        this.fullName = fullName;
        this.role = role;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public int getRole() {
        return role;
    }

    public void setRole(int role) {
        this.role = role;
    }

}
